class RGB(Color):
    def hex_format(self):
        '''Returns color in hex format'''
        return '#{:02X}{:02X}{:02X}'.format(self.red,self.green,self.blue)
    def int_format(self):
        return (self.red << 16) +(self.green<<8) + self.blue

